class Solution:
    def generate(self, সারিগুলি_সংখ্যা: int) -> List[List[int]]:
        ত্রিভুজ = []  

        for সারি_ইন্ডেক্স in range(সারিগুলি_সংখ্যা):  

            সারি = [1] * (সারি_ইন্ডেক্স + 1) 

            for জ in range(1, সারি_ইন্ডেক্স):  
                সারি[জ] = ত্রিভুজ[সারি_ইন্ডেক্স - 1][জ - 1] + ত্রিভুজ[সারি_ইন্ডেক্স - 1][জ]

            ত্রিভুজ.append(সারি)  

        return ত্রিভুজ 
